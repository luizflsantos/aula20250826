vMes = int(input("Digite o número do mês (1-12): "))
if vMes == 1:
    print("Janeiro")
elif vMes == 2:
    print("Fevereiro")
elif vMes == 3:
    print("Março")
elif vMes == 4:
    print("Abril")
elif vMes == 5:
    print("Maio")
elif vMes == 6:
    print("Junho")
elif vMes == 7:
    print("Julho")
elif vMes == 8:
    print("Agosto")
elif vMes == 9:
    print("Setembro")
elif vMes == 10:
    print("Outubro")
elif vMes == 11:
    print("Novembro")
elif vMes == 12:
    print("Dezembro")
else:
    print("Mês inválido.")
